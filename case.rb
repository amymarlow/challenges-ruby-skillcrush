weather = "sunny"
case(weather)
when "raining" then puts "Wear your rubber boots and rain jacket!"
when "snowing" then puts "Wear your hat and scarf"
when "sunny" then puts "Get your swimsuit on and head to the beach!"
when "cloudy" then puts "Leave your PJ's on and go back to bed!"
when "foggy" then puts "Wear a light jacket"
else puts "Wear your birthday suit and party!"
end